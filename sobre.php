<span class="fundacao"> <strong>  
  
CENTRO DE SAÚDE DO CASSEL,foi fundado no ano de 1988 pelo Ministério da Saúde
afim de ajudar a todos os codadãos do distrito urbano da Maianga,especificamente
do Cassequel a ter um melhor acapanhamento concernente a diversas patologias,
que ameaça a saúde de toda a população.Sobe missão de savalguardar a vida de 
todo o paciente.Actualmente o Centro conta com diversas especialidades médicas
para assim atender pacientes com variadas patologias.
<hr>
<br>
No entanto,o hospital conta com 8 médicos e 10 doctores,com a subdivisão em 4 blocos
ou áreas específicas de atendimento.
<hr>
<br>
Atendemos aproximadamente 50 pacientes ao dia em cada área,com processos de triagem 
antes de seres consultado.
